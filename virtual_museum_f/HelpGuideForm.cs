using System.Drawing;
using System.Windows.Forms;

namespace virtual_museum_f
{
    public partial class HelpGuideForm : Form
    {
        public HelpGuideForm()
        {
            InitializeComponent();
            InitializeHelpGuide();
        }

        private void InitializeHelpGuide()
        {
            this.Text = "Οδηγός Βοήθειας Εικονικού Μουσείου";
            this.Size = new Size(800, 600);

            treeView.Dock = DockStyle.Left;
            treeView.Width = 250;

            richTextBox.Dock = DockStyle.Fill;
            richTextBox.ReadOnly = true;
            richTextBox.Font = new Font("Segoe UI", 10);

            PopulateTreeView();
            treeView.AfterSelect += TreeView_AfterSelect;
        }

        private void PopulateTreeView()
        {
            treeView.Nodes.Add("Αρχική Σελίδα");
            treeView.Nodes.Add("Αίθουσα Συναυλιών");
            treeView.Nodes.Add("Αίθουσες Καλλιτεχνών");
            treeView.Nodes.Add("Καφετερία-Εστιατόριο");
            treeView.Nodes.Add("Είσοδος Υπαλλήλων");
            treeView.Nodes.Add("Αίθουσες Προβολής Βιντεοσκοπημένων Εκδηλώσεων");
            treeView.Nodes.Add("Ιδιωτικοί Χώροι Προβολής (DJ)");
            treeView.Nodes.Add("Προσομοίωση αλληλεπίδρασης με τις διάφορες συσκευές");
        }

        private void TreeView_AfterSelect(object sender, TreeViewEventArgs e)
        {
            richTextBox.Text = GetHelpContent(e.Node.Text);
        }

        private string GetHelpContent(string topic)
        {
            switch (topic)
            {
                case "Αρχική Σελίδα":
                    return "Καλώς ήρθατε στο Εικονικό Μουσείο!\n\n" +
                           "Στην αρχική σελίδα θα βρείτε τα ακόλουθα κουμπιά:\n\n" +
                           "- ΑΙΘΟΥΣΑ ΣΥΝΑΥΛΙΩΝ\n" +
                           "- ΑΙΘΟΥΣΕΣ ΚΑΛΛΙΤΕΧΝΩΝ\n" +
                           "- ΚΑΦΕΤΕΡΙΑ-ΕΣΤΙΑΤΟΡΙΟ\n" +
                           "- ΕΙΣΟΔΟΣ ΥΠΑΛΛΗΛΩΝ\n" +
                           "- ΑΙΘΟΥΣΕΣ ΠΡΟΒΟΛΗΣ ΒΙΝΤΕΟΣΚΟΠΗΜΕΝΩΝ ΕΚΔΗΛΩΣΕΩΝ\n" +
                           "- ΙΔΙΩΤΙΚΟΙ ΧΩΡΟΙ ΠΡΟΒΟΛΗΣ (DJ)\n" +
                           "- ΠΡΟΣΟΜΟΙΩΣΗ ΑΛΛΗΛΕΠΙΔΡΑΣΗΣ ΜΕ ΤΙΣ ΔΙΑΦΟΡΕΣ ΣΥΣΚΕΥΕΣ\n\n" +
                           "Επιλέξτε ένα κουμπί για να μεταβείτε στην αντίστοιχη ενότητα.";

                case "Αίθουσα Συναυλιών":
                    return "Στην Αίθουσα Συναυλιών μπορείτε να δείτε πληροφορίες για επερχόμενες συναυλίες και εκδηλώσεις.";

                case "Αίθουσες Καλλιτεχνών":
                    return "Στις Αίθουσες Καλλιτεχνών μπορείτε να εξερευνήσετε τους χώρους διαφόρων καλλιτεχνών.\n\n" +
                           "Κάντε κλικ στον χάρτη για να επισκεφθείτε τις αίθουσες των καλλιτεχνών:\n" +
                           "- Αίθουσα Καλλιτέχνη 1\n" +
                           "- Αίθουσα Καλλιτέχνη 2\n" +
                           "- Αίθουσα Καλλιτέχνη 3\n" +
                           "- Αίθουσα Καλλιτέχνη 4";

                case "Καφετερία-Εστιατόριο":
                    return "Στην ενότητα Καφετερία-Εστιατόριο μπορείτε να δείτε το μενού και τις υπηρεσίες που προσφέρονται.";

                case "Είσοδος Υπαλλήλων":
                    return "Η Είσοδος Υπαλλήλων είναι μόνο για εξουσιοδοτημένο προσωπικό. Εδώ οι υπάλληλοι μπορούν να συνδεθούν στο σύστημα και να διαχειριστούν κρατήσεις, παραγγελ.";

                case "Αίθουσες Προβολής Βιντεοσκοπημένων Εκδηλώσεων":
                    return "Στις Αίθουσες Προβολής Βιντεοσκοπημένων Εκδηλώσεων μπορείτε να παρακολουθήσετε βίντεο από παλαιότερες εκδηλώσεις και συναυλίες.";

                case "Ιδιωτικοί Χώροι Προβολής (DJ)":
                    return "Στους Ιδιωτικούς Χώρους Προβολής (DJ) μπορείτε να κάνετε κράτηση για ιδιωτικές εκδηλώσεις και να προσαρμόσετε τη μουσική και το περιβάλλον.";

                case "Προσομοίωση αλληλεπίδρασης με τις διάφορες συσκευές":
                    return "Στην ενότητα Προσομοίωσης μπορείτε να αλληλεπιδράσετε με διάφορες συσκευές του μουσείου, όπως:\n\n" +
                           "- Έλεγχος φωτισμού\n" +
                           "- Ρύθμιση θερμοκρασίας\n" +
                           "- Έλεγχος ήχου\n" +
                           "- Άλλες συσκευές του μουσείου";

                default:
                    return "Επιλέξτε ένα θέμα από το μενού στα αριστερά για να δείτε περισσότερες πληροφορίες.";
            }
        }
    }
}